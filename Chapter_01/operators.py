################# Arithmetic Operators ##################

a = 2
b = 4

print(a + b)
print(a - b)
print(a * b)
print(a**b)
print(a / b)
print(a // b)
print(a % b)

################# Relational / Comparison Operators ##################

print(a == b)
print(a != b)
print(a > b)
print(a < b)
print(a >= b)
print(a <= b)

################# Assignment Operators ##################

num1 = 3
num1 += 20
num1 -= 20
num1 *= 20
num1 /= 20
num1 %= 20
num1 **= 2

print(num1)


################# Logical Operators ##################

val = False
val1 = True

print("AND Operator", (val) and (val1))
print("OR Operator", (val) or (val1))
print("NOT Operator", not (val))
